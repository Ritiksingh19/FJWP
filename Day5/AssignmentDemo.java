// Assignment Operators

class AssignmentDemo
{

	public static void main(String args[])

{

	int a = 0;

        System.out.println("a :" +a);
	a+=10;
	
	System.out.println("a :" +a);
	a-=20;

	System.out.println("a :" +a);
	a*=10;

	System.out.println("a :" +a);
	a/=2;

	System.out.println("a :" +a);
	a%=4;

 	System.out.println("a :" +a);
}
}