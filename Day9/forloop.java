public class forloop {
    public static void main(String[] args) {
        for(int i = 0 ; i<=10 ; i++){
            System.out.println(i);
        }
        int x = 1;
        for(; x<=10;){
            System.out.println("hello");
        }
        for(;;){
            System.out.println("hello");
        }
    }
}
